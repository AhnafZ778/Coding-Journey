package Loops_Conditionals;

public class checking {
    public static void main(String[] args) {
        int count = 0;
        while (count < 3){
            System.out.println(count);
            count++;
        }
        int count2 = 0;
        do {
            System.out.println(count2);
            count2++;
        } while(count2 < 3);
    }
}
